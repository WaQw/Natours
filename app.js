// refactor routers to reduce url's existence
app.route('/api/v1/tours').get(getAllTours).post(createTour);

app
  .route('/api/v1/tours/:id')
  .get(getTour)
  .patch(updateTour)
  .delete(deleteTour);

port = 3000;
app.listen(port, () => {
  console.log(`App running on port ${port}...`);
});
